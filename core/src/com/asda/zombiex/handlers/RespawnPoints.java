package com.asda.zombiex.handlers;

/**
 * @author Skala
 */
public class RespawnPoints {
    private float x, y;

    public RespawnPoints(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
